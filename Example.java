// This is a Example class which will demo
//displaying output from a Java program
//Your class names in Java always start with the uppercase letter
public class Example
{
   //This is the main method
   public static void main (String[] args)
   {
      //This is an output statement
      System.out.println ("Hello World!");
      
      System.out.println ("This is line 1");
      System.out.println ("This is line 2");
      
      System.out.print ("This is line 1\n");
      System.out.println ("This is line 2");
      
      System.out.print ("First\tLast\n");
      System.out.println ("Kendra\tBrauer");
      
      System.out.print ("My age is: ");
      System.out.println (30);
   }
}